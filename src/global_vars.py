import re
#Flags passadas pelo utilizador como input.
flags = {}

'''
Ex:
    lines=ALL
    lines=red
    lines=green
    lines=blue
    lines=yellow
'''

def add_flags(args):
    p = re.compile('lines=+')
    flags["verbose"] = None
    for arg in args:
        if(p.match(arg) != None):
            arg = arg.split("=")[1]
            if (arg not in ["yellow","green","blue","red","ALL"]): #erro
                print("FLAG: " + str(arg) + "não reconhecida pelo sistema")
                #exit()
            else: flags["verbose"] = arg


